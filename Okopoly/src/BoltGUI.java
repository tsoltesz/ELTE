    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        if (((AbstractButton)evt.getSource()).getModel().isSelected() ){
            if (!g.jatekosok.get(g.aktualisjatekos).getAuto()){
            jToggleButton3.setEnabled(true);
            jToggleButton3.setSelected(true);
            jToggleButton4.setEnabled(true);
            jToggleButton4.setSelected(false);
            if (!elektromos && !benzines){
                benzines=true;
                fizetendo+=5000000;
            }}
            else {
                jToggleButton4.setSelected(true);
                elektromos=true;
                fizetendo+=5000000;
            }
        }
        else if (g.jatekosok.get(g.aktualisjatekos).getAuto()){
            jToggleButton4.setSelected(false);
            elektromos=false;
            fizetendo-=5000000;
        }
        else {
            jToggleButton3.setEnabled(false);
            jToggleButton3.setSelected(false);
            jToggleButton4.setEnabled(false);
            jToggleButton4.setSelected(false);
            if (benzines){
                benzines=false;
                fizetendo-=5000000;
            }
            else if (elektromos){
                elektromos=false;
                fizetendo-=10000000;
            }
            
        }
        jLabel1.setText("Összeg: "+kiir(fizetendo)+" Ft");
    }//GEN-LAST:event_jToggleButton1ActionPerformed

