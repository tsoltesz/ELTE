    public String kiir(int sz){
        int szamlalo=0;
        String s=Integer.toString(sz);
        String k="";
        for (int i=s.length()-1;i>=0;i--){
           if (szamlalo==3){
                k="."+k;
                szamlalo=0;
            }
        
            k=s.substring(i,i+1)+k;
            szamlalo++;
        }
        return k;
    }
