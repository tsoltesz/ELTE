    public void Szamkijeloles(int szam){
        int db=0;
        for (int i=0;i<szamok.length;i++){
            if(szamok[i]){
                db++;
            }
        }
        
        String s="";
        
        if(db<7 && szamok[szam]==false){
            szamok[szam]=true;
            panelek[szam].setBackground(Color.red);
            labelek[szam].setForeground(Color.white);
            if (db+1==7){
                jButton1.setEnabled(true);
            }
        }
        else if (szamok[szam]){
            szamok[szam]=false;
            panelek[szam].setBackground(Color.white);
            labelek[szam].setForeground(Color.red);
            if(db-1<7){
                jButton1.setEnabled(false);
            }
        }
        for (int i=0;i<szamok.length;i++){
            if (szamok[i]){
                s=s+=(i+1)+" ";
            }
        }
        jLabel9.setText(s);
    }

